import "server-only"

/** Lowercase, ASCII, hyphen-separated slug — used for `stores.slug` (globally unique, immutable). */
export function slugify(input: string): string {
  return input
    .normalize("NFD")
    .replace(/[̀-ͯ]/g, "")
    .toLowerCase()
    .trim()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "")
}
